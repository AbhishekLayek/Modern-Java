package Java15;

public class TextBlock {
	public static void main(String[] args) {
		
		String text = """
					  Hello Everyone,
					  This Is Java Text Block.
					  It Supports Multiple Line.
					  """;
		
		System.out.println(text);
	}
}

/*
 * Output:
 * 
 * Hello Everyone,
 * This Is Java Text Block.
 * It Supports Multiple Line.
 */